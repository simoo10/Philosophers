#include"philo.h"

int	ft_atoi(const char *str)
{
	long long	c;
	int			i;

	i = 1;
	c = 0;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == 43 || *str == 45)
	{
		if (*str == 45)
			i = -i;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		c = c * 10 + *str++ - '0';
		if (c * i > 2147483647)
			return (-5);
		if (c * i < -2147483648)
			return (-7);
	}
	c = c * i;
	i = c;
	return (i);
}
